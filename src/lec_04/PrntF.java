package lec_04;

public class PrntF {
    static void main() {

                // ============================================
                // %s — рядок (String)
                // ============================================

                String name = "Олександр";
                System.out.printf("Ім'я: %s%n", name);


                // ============================================
                // %d — ціле число
                // ============================================

                int age = 35;
                System.out.printf("Вік: %d%n", age);


                // ============================================
                // %f — число з плаваючою комою
                // ============================================

                double salary = 12345.6789;

                // За замовчуванням 6 знаків після коми
                System.out.printf("Зарплата: %f%n", salary);

                // Два знаки після коми
                System.out.printf("Зарплата: %.2f%n", salary);


                // ============================================
                // %c — символ
                // ============================================

                char grade = 'A';
                System.out.printf("Оцінка: %c%n", grade);


                // ============================================
                // %b — boolean
                // ============================================

                boolean active = true;
                System.out.printf("Активний: %b%n", active);


                // ============================================
                // %x — шістнадцяткове число
                // ============================================

                int number = 255;

                System.out.printf("HEX: %x%n", number);
                System.out.printf("HEX великими літерами: %X%n", number);


                // ============================================
                // %o — вісімкове число
                // ============================================

                System.out.printf("Octal: %o%n", number);


                // ============================================
                // %% — символ %
                // ============================================

                System.out.printf("Знижка: 20%%%n");


                // ============================================
                // %n — перенос рядка
                // ============================================

                System.out.printf("Перший рядок%nДругий рядок%n");


                // ============================================
                // Виведення декількох змінних
                // ============================================

                String city = "Київ";
                int population = 3000000;

                System.out.printf(
                        "Місто: %s, Населення: %d%n",
                        city,
                        population
                );


                // ============================================
                // Вирівнювання по правому краю
                // ============================================

                System.out.printf("|%10d|%n", 25);

                // Результат:
                // |        25|


                // ============================================
                // Вирівнювання по лівому краю
                // ============================================

                System.out.printf("|%-10d|%n", 25);

                // Результат:
                // |25        |


                // ============================================
                // Мінімальна ширина рядка
                // ============================================

                System.out.printf("|%15s|%n", "Java");

                // |           Java|


                // ============================================
                // Вирівнювання рядка по лівому краю
                // ============================================

                System.out.printf("|%-15s|%n", "Java");

                // |Java           |


                // ============================================
                // Доповнення нулями
                // ============================================

                System.out.printf("%05d%n", 42);

                // 00042


                // ============================================
                // Форматування дробових чисел
                // ============================================

                double pi = Math.PI;

                System.out.printf("%.1f%n", pi);
                System.out.printf("%.2f%n", pi);
                System.out.printf("%.3f%n", pi);
                System.out.printf("%.4f%n", pi);


                // ============================================
                // Таблиця
                // ============================================

                System.out.printf("%-15s %-10s %-12s%n",
                        "Ім'я",
                        "Вік",
                        "Зарплата");

                System.out.printf("%-15s %-10d %-12.2f%n",
                        "Іван",
                        25,
                        1500.50);

                System.out.printf("%-15s %-10d %-12.2f%n",
                        "Марія",
                        31,
                        2700.99);


                // ============================================
                // Виведення чисел у різних системах
                // ============================================

                int value = 100;

                System.out.printf("Десяткова: %d%n", value);
                System.out.printf("Шістнадцяткова: %x%n", value);
                System.out.printf("Вісімкова: %o%n", value);


                // ============================================
                // Комбінація форматів
                // ============================================

                String product = "Ноутбук";
                double price = 15999.99;
                int quantity = 3;

                System.out.printf(
                        "Товар: %-15s Ціна: %10.2f Кількість: %3d%n",
                        product,
                        price,
                        quantity
                );


                // ============================================
                // Індекс аргументів
                // ============================================

                System.out.printf(
                        "%2$s коштує %1$.2f грн%n",
                        150.50,
                        "Книга"
                );

                // Книга коштує 150.50 грн


                // ============================================
                // Розділювач тисяч
                // ============================================

                int money = 12500000;

                System.out.printf("%,d%n", money);

                // 12,500,000 (або інший формат залежно від Locale)


                // ============================================
                // Форматування часу
                // ============================================

                System.out.printf(
                        "Час: %02d:%02d:%02d%n",
                        8,
                        5,
                        9
                );

                // 08:05:09
            }
        }
